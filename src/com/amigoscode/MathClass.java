package com.amigoscode;

public class MathClass {

	public static void main(String[] args) {
		System.out.println("Result: " + Math.abs(-100));
		System.out.println("Max: " + Math.max(100,200));
		System.out.println("Min: " + Math.min(3.6,5.5));
		System.out.println("Pow: " + (int) Math.pow(5, 2));
		System.out.println("sqrt: " + (int) Math.sqrt(36));
		System.out.println("PI: " + Math.PI);
	}
}
